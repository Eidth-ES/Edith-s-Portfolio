let currentExperienceId = null;
let resizeRaf = null;

function initExperiences() {
    closeExperienceDetail();
    const listContainer = document.getElementById('experiences-list');
    listContainer.innerHTML = '';
    experiences.forEach((item, index) => {
        const itemEl = createExperienceItem(item, index);
        listContainer.appendChild(itemEl);
    });

    initFloatingTags();
}

function createExperienceItem(item) {
    const div = document.createElement('div');
    div.className = 'experience-item';
    div.dataset.id = item.id;
    div.dataset.categories = item.categories.join(',');

    const mascotInner = item.mascotImage
        ? `<img src="${item.mascotImage}" alt="" />`
        : (item.mascotEmoji || "");

    div.innerHTML = `
        <div class="experience-bg" style="background-color: ${item.accentColor}"></div>
        <div class="experience-content">
            <h2 class="experience-title">${item.title}</h2>
            <span class="experience-company">${item.company}</span>
        </div>
        <div class="experience-mascot">
            <div class="mascot-placeholder">${mascotInner}</div>
        </div>
    `;

    div.addEventListener('mouseenter', () => handleMouseEnter(div, item));
    div.addEventListener('mouseleave', () => handleMouseLeave(div));
    div.addEventListener('click', () => openExperienceDetail(item.id, div));
    return div;
}

function handleMouseEnter(element, item) {
    const bg = element.querySelector('.experience-bg');
    const title = element.querySelector('.experience-title');
    const company = element.querySelector('.experience-company');

    if (bg && item.hoverBgColor) bg.style.backgroundColor = item.hoverBgColor;
    if (title) title.style.removeProperty('color');
    if (company) {
        company.style.removeProperty('border-color');
        company.style.removeProperty('color');
    }
}

function handleMouseLeave(element) {
    const bg = element.querySelector('.experience-bg');
    const title = element.querySelector('.experience-title');
    const company = element.querySelector('.experience-company');

    const experienceId = element?.dataset?.id;
    const item = experiences.find(x => x.id === experienceId);
    if (bg && item?.accentColor) bg.style.backgroundColor = item.accentColor;
    if (title) title.style.removeProperty('color');
    if (company) {
        company.style.removeProperty('border-color');
        company.style.removeProperty('color');
    }
}

function initFloatingTags() {
    const tags = document.querySelectorAll('.tag-block');
    const items = document.querySelectorAll('.experience-item');

    tags.forEach(tag => {
        const category = tag.dataset.category;

        tag.addEventListener('mouseenter', () => {
            if (currentExperienceId) return;
            items.forEach(item => {
                const itemCategories = item.dataset.categories.split(',');
                if (itemCategories.includes(category)) {
                    item.classList.add('highlight');
                } else {
                    item.style.opacity = '0.3';
                }
            });
        });

        tag.addEventListener('mouseleave', () => {
            if (currentExperienceId) return;
            items.forEach(item => {
                item.classList.remove('highlight');
                item.style.opacity = '1';
            });
        });
    });
}

function openExperienceDetail(experienceId, clickedElement) {
    const experience = experiences.find(x => x.id === experienceId);
    if (!experience) return;

    const container = document.getElementById('experiences-container');
    const detail = document.getElementById('experiences-detail');

    currentExperienceId = experienceId;

    if (clickedElement) {
        clickedElement.classList.add('is-opening');
        window.setTimeout(() => clickedElement.classList.remove('is-opening'), 520);
        spawnOpeningLeaf(clickedElement);
    }

    container.classList.add('is-detail');
    detail.setAttribute('aria-hidden', 'false');
    detail.classList.remove('is-entering');
    renderExperienceDetail(experience, detail);
    detail.scrollTop = 0;

    requestAnimationFrame(() => {
        detail.classList.add('is-entering');
        window.setTimeout(() => detail.classList.remove('is-entering'), 600);
    });
}

function closeExperienceDetail() {
    const container = document.getElementById('experiences-container');
    const detail = document.getElementById('experiences-detail');

    currentExperienceId = null;
    container.classList.remove('is-detail');
    detail.setAttribute('aria-hidden', 'true');
    detail.innerHTML = '';
    detail.style.backgroundColor = '';
    detail.style.removeProperty('--exp-back-bg');
    detail.style.removeProperty('--exp-scale');
}

function closeDetail() {
    closeExperienceDetail();
}

